class Calculator{ //clase que permite realizar las operaciones en la calculadora.
    //suma
    sumar(valor1, valor2){
        return valor1 + valor2;
    }
    //resta
    restar(valor1, valor2){
        return valor1 - valor2;
    }
    //dividir
    dividir(valor1, valor2){
        return valor1 / valor2;
    }
    //multiplicar
    multiplicar(valor1, valor2){
        return valor1 * valor2;
    }
    
}